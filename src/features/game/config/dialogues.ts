export const pickNpcQuestion = (npcType: NpcType): string => {
	const variants = NPC_QUESTIONS[npcType]
	if (!variants.length) return ''
	const index = Math.floor(Math.random() * variants.length)
	return variants[index]
}

export const pickExcuseText = (type: ExcuseType): string => {
	const variants = EXCUSE_TEXTS[type]
	if (!variants.length) return type
	const index = Math.floor(Math.random() * variants.length)
	return variants[index]
}
